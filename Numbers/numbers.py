# Comparing Numbers
print("Comparing Numbers")

x = 10
y = 20

print("x = {}".format(x))
print("y = {}".format(y))

print("x = {} y = {}".format(x,y))

if y > x:
    print("{} is greater than {}".format(y,x))
else:
    print("{} is greater than {}".format(x,y))
    
    
# Arithmetic Operations
print("Operations on Numbers")

# Addition
sum = x + y
print("Addition : {}".format(sum))

# Subtraction
diff = y - x
print("Subtraction : {}".format(diff))

# Multiplacation
product = x * y
print("Multiplacation : {}".format(product))

# Division
quotient = y / x
print("Division : {}".format(quotient))

# Modulus
modulus = x % y
print("Remainder : {}".format(modulus))

# Exponentation
expo = x ** y
print("Exponentation Method : {}".format(expo))

# Floor Division
fldiv = x // y
print("Floor Division : {}".format(fldiv))


